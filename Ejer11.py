"""  Ejercicio 11
Media de notas
Pide tres notas, calcula media, muestra nota final """

nota1 = int(input("Introduzca su primera nota: "))
nota2 = int(input("Introduzca su segunda nota: "))
nota3 = int(input("Introduzca su tercera nota: "))

nota_media = (nota1 + nota2 + nota3) / 3

print(f"La nota media es {nota_media}.")